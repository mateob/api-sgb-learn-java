package com.api.sgb.learnjava.math;

public class SampleMath {
    public Double sum(Double numOne, Double numTow) {
        return numOne + numTow;
    }

    public Double subtraction(Double numOne, Double numTow) {
        return numOne - numTow;
    }

    public Double multiplication(Double numOne, Double numTow) {
        return numOne * numTow;
    }

    public Double division(Double numOne, Double numTow) {
        return numOne / numTow;
    }

    public Double average(Double numOne, Double numTow) {
        return (numOne + numTow) /2;
    }

    public Double squareRoot(Double number) {
        return Math.sqrt(number);
    }
}
